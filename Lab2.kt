fun main() {
    // Завдання 1: Повернення списку квадратів чисел
    val numbers = listOf(1, 2, 3, 4, 5)

    // Використовуємо функцію map для отримання квадратів кожного числа
    val squares = numbers.map { it * it }
    println("1. Квадрати чисел: $squares")  // Виведе: [1, 4, 9, 16, 25]

    // Завдання 1а: Знайти суму квадратів
    // Використовуємо функцію sumOf для підсумовування квадратів чисел
    val sumOfSquares = numbers.sumOf { it * it }
    println("1а. Сума квадратів: $sumOfSquares")  // Виведе: 55

    // Завдання 2: Повернення всіх пар чисел з двох списків
    val list1 = listOf(1, 2, 3)
    val list2 = listOf(3, 4)

    // Використовуємо flatMap та map для створення всіх можливих пар
    val pairs = list1.flatMap { a -> list2.map { b -> a to b } }
    println("2. Усі пари чисел: $pairs")  // Виведе: [(1, 3), (1, 4), (2, 3), (2, 4), (3, 3), (3, 4)]
}
